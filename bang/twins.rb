<HEAD1>dui yamajer kAhinI</HEAD1>  
<LINK to="https://www.poemhunter.com/poem/the-twins-6/">@{<E>The Twins</E>@}</LINK>  nAme ekTA
 i.nrAji hAsir kabitA Achhe. kabir nAm @{<E>Henry Sambrooke Leigh.</E>@}  eTAr ekTA bhAbAnubAd karechhi, eirakam--


 AmrA du bhAi emanai yamaj, tAjjab sab loke
oke dekhe loke Ami bale bhAbe, AmAke dekhile oke.
Asal nakal bojhAr dhakale sakale kAhil bhAri.
byApArkhAnA yA d,nARAlo beyARA, kemane bojhAte pAri. 
nAm diye tAo chenA yeta, tabu moder bAper bhule
se pathao bandha (barAt manda!), byApArTA bali khule--
bAbA bhebechhila Ami bhUto haba, Ar bhAi habe Ty,nApA 
kintu niyati niThur yakhan ke ThekAbe tAr hyApA?
dukhAnA bAchchA bemAlum ekai, duijane dui kole
bAbA bechArAr bibhram hala kAke kI DAkibe bale.
se ki mahAbhul! gela bilkul sab AiDenTiTi 
pitAr pramAde pAr+mAnen+Tli paranAme parichiti!
se ye ki jhAmelA nAhi yAy balA, pade pade tAr g,nuto.
yata bali bhAi bhUto nay, tabu loke DAke tAke bhUto. 
nAmer du.Hkha tAo saoyA yeta, kintu sheS~TA bhAyA 
eman kAnDa ghaTila  yAhAte bujhinu jagata mAyA.
bhUto (mAne iye Asale ye T,nyApA) atIba bakhATe chhele 
iskule tAr kapAle prahAr juTita dharite pele 
kintu sUxma eTAi du.Hkha dharite nA pere oke
kono doS chhARA AmAke bechArA ThyA\ngAta sakal loke. 
k\oishor~sheSe y\ouban ele ghaTakerA kari yatna 
kh,noj dila yAhA, ki baliba AhA, rUpasI ramaNI ratna! 
mane hala bujhi etadine mor poRA bhAl sheSe khulla.
sekhAneo hAy amogh niyati (adhik balA bAhulya)!
y\ouban gele madhyabayas, ebhAbe gaRAte gaRAte 
sabAr yA hay, AmArao tA hala-- TheklAm ese jarAte. 
ekdin dekhi pARAr lokerA chitAy tulechhe bhAike,
''AhA bhUtobAbu swar+ge gelen'', khub ch,nyAchAchchhe mAike !
bhAiyer du.Hkhe k,nAd~ba ki chhAi, niyati ekhanao chhAReni--
bhAi to mareni, marechhi Amii, bujhtei orA pAreni! 

