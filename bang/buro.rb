<HEAD1>buRo khokAr swap+na</HEAD1>

AhA, yadi sei nabaY\ouban<BR/>
AbAr Asita phire!<BR/>
rAjmukuT ke chAhita takhan<BR/>
parite palita shire?<BR/>

bayser bali-kunchan~rekhA<BR/>
jnAnbrriddher bhAle<BR/>
bahu bachharer sanchita shekhA<BR/>
chh,nuRe phele janjAle--<BR/>

muhUr+tamAjhe neche uThitAm<BR/>
bAlyer uchchhwAse!<BR/>
AhA sei sukh, swap+nasamAn,<BR/>
satyai yadi Ase!<BR/>

kimAshcharYa sei shubhaxaN!<BR/>
debatAr dUt dwAre<BR/>
AsiyA hAsiyA kahila--swapan<BR/>
satya hatei pAre!<BR/>

jIrNa e jarA habe itihAs<BR/>
bArdhakyer iti--<BR/>
bhebe dyAkh.h kichhu rekhe dite chAs.h?<BR/>
bhabiSyater smrriti?<BR/>

mane paRe gela patnIr prem<BR/>
AhA, priyatamA nArI,<BR/>
kata baraSer nikaSita hem<BR/>
tAre ki chhARite pAri!<BR/>

s+warNa Akhare likhila santa,<BR/>
madhur hAsyadhAre--<BR/>
brriddha haibe bAlak kintu<BR/>
jAyA-samabhibyAhAre!<BR/>

likhiyA chAhila puna pAne mor--<BR/>
Ar kichhu chAs.h sAthe?<BR/>
jarA yAbe yabe rabe nA ko tor<BR/>
Ar kono kichhu hAte!<BR/>

nA, nA, chAi baTe, chhele meye kaTi,<BR/>
oder ki kare chhARi!<BR/>
o go deb~dUt kari go minati<BR/>
oder rAkhite pAri?<BR/>

santa takhan tyajiyA lekhnI<BR/>
kahila uchchahAse--<BR/>
tui yadi, bokA,  nije khokA habi<BR/>
tor khokA kise Ase?<BR/>

Apan kAnDe hese uThi yei<BR/>
ghum gela mor TuTe<BR/>
buRoder tare e kabitA tAi<BR/>
likhechhi sakAle uThe.<BR/>

