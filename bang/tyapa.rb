<HEAD1>si.nha-T,nyApA sa.nbAd</HEAD1>
<PRE>
sAgarpAre dibyi khAsA jAygA
bombe theke ghan+TA duyek rele
paTalbAbu kar+tA eba.n ginnI,
sethAy gelen sange niye chhele.

shrImAn T,nyApA nehAt takhan bAchchA,
kintu T,nyApAr bhaye sakalei tras+ta.
koththeke ek juTiye lAThi rAs+tAy
bhAbTA yena keukeTA ek mas+ta!

sAgar dekhe ginnI balen duchchhAi,
paysAgulo bebAk gela jale
Dobeo nA keu em.hni @{<E>boring</E>@} jAygA
emanbhAbe kadin thAkA chale?

paTal, T,nyApA eba.n paTal ginnI
chiRiyAkhAnA dekhte gelen ghure
si.nha-Ti.nha chhARAo kinA bis+tar
khAbAr dokAn se jAygATA juRe!

mas+ta kh,nAchAy ghumochchhila si.nha
munDu thuye garAdgulor pare
k,nApiye diye kh,nAchAr deyAl suddha
nAk Deke tAr bishrI moTA sware.

chhabir baiye si.nha dekhe T,nyApA
jAn~ta tArA AfrikAte thAke
cham.hke gela dekhte pela yabe
si.nha AbAr nAk~o nAki DAke!

T,nyApAr nAkTA Thik Tikalo nay to
yei dekhechhe si.nherao nAk b,nochA
saTAn giye hAter lAThi diye
lAgiyechhe  tAy moxam ek kh,nochA!

kAr mejAjer Thik bA bala thAke
nAker pare eman kh,nochA dile?
si.nhamashAy khapA.t kare  dhare
gapA.t kare phella T,nyApAy gile!

kAnDa dekhe paTalbAbu cham.hke
chashmAchokhe bejAy giye ghAbRe
Ar+tarabe balen--o go ginnI,
si.nha tomAr T,nyApAy dila sAbRe!

ginnI balen--ki sA.nghAtik tAi to,
anAsrriSTi kAnDa ekTA mAiri!
ei nA bale kar+tA-ginnI mile
pulish thAnAy karte gelen DAiri.

pulish balle--oi chhele ye ApnAr
keman kare jAnba seTA kh,nATi?
paTlabAbu balen chaTe--pramAN?
kh,nAchAr bAire ei ye T,nyApAr lAThi!

baRabAbu talab peye ese
balen h,neke--mashAy kena kruddha?
paTal balen--si.nhabyATA kinA
T,nyApAy khela natun kApaRsuddha!

ginnI balen--ki bhadratA eTA?
eichi nAki bAner jale bhese?
kon Ak+kele T,nyApAr gela prANTA
paysA phele TikiT keTe ese?

baRabAbu TAkAr thali khule
balen--katoy chalbe ApAtata.H?
paTalbAbu jiges kare shune--
chalti reTTA yAchchhe ekhan kata?

ginnI shune balen xepe--Ami
sAn.hkibhA\ngAr xyAntamani dAsI!
AmAr bAchhAy si.nhe khAbe am.hni?
ekTA kAro hatei habe f,nAsI!

sabAi gela myAjis+T+reTer kAchhe
kAnDakhAnA balle paripATi.
pramANs+warUp myAjis+T+reTer hAte
paTla dilen T,nyApan sonAr lAThi.

dharmAbatAr nasyi niye nAke
balen--kAro kasur nAhi ete,
ekTi bachhar lAgbe baRajor
paTalbAbur AbAr chhele hate.

paTal~ginnI xepe halen bom.h--
thAk.h thAk.h, khub maskarA, nA, seTA?
bAchchA biyoi si.nhe khAbe bale,
buRo belAy AmAy khAoyAy keTA?
