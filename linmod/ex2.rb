<HEAD1>Exercises on identifiability</HEAD1>
<OL><LI>
Consider the model <M>y_{ij} = \mu + \alpha_i +
\epsilon_{ij},</M> where <M>i=1,2,3</M> and <M>j=1,...,5.</M> We
assume the Gauss-Markov set up. State with reason which of the
following parametric functions are estimable:
<OL>
<LI><M>\alpha_1-\alpha_3</M></LI>
<LI><M>\mu+\alpha_1</M></LI>
<LI><M>\mu-\alpha_2</M></LI>
<LI><M>\alpha_1-2 \alpha_2+\alpha_3</M></LI>
</OL>
</LI>

<LI>Same set up as above. Is it true that all parametric
functons of the form <M>\sum_i c_i \alpha_i</M> with <M>\sum c_i
= 0</M>, are estimable. Also, is it true that all such parametric
functons must be non-estimable if <M>\sum c_i\neq 0?</M>
</LI>

<LI>Consider the Gauss-Markovian linear model <M>y_{ijk} = \mu +
\alpha_i + \beta_j + \epsilon_{ijk},</M> where <M>i=1,...,I</M>
and <M>j=1,..,J</M> and <M>k=1,...,n_{ij} (\geq 2).</M> State with reason which of the
following parametric functions are estimable:
<OL>
<LI><M>\alpha_i-\alpha_j</M> for <M>i\neq j.</M></LI>
<LI><M>\alpha_i+\alpha_j</M> for <M>i\neq j.</M></LI>
<LI><M>\beta_i-\beta_j</M> for <M>i\neq j.</M></LI>
<LI><M>\mu+\alpha_i</M></LI>
<LI><M>\mu-\alpha_i</M></LI>
<LI><M>\mu+\beta_j</M></LI>
<LI><M>\mu+\alpha_1+\beta_2</M></LI>
</OL>
</LI>

<LI>Same set up as in the last problem. 
<OL>
<LI>If <M>\mu + \sum c_i \alpha_i</M> is estimable:, then what
can you say about <M>c_i</M>'s?</LI>
<LI>If <M>\sum c_i \alpha_i</M> is estimable:, then what
can you say about <M>c_i</M>'s?</LI>
<LI>If <M>\mu + \sum c_i \alpha_i + \sum d_j \beta_j</M> is estimable:, then what
can you say about <M>c_i</M>'s and <M>d_j</M>'s?</LI>
</OL>
</LI>

<LI>Same set up as above. Find the dimension of the space of all
identifiable linear parametric functions. How does the answer
change if all the <M>n_{ij}</M>'s are known to be equal?</LI>
</OL>

