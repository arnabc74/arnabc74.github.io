#include <stdio.h>

int main() {
  int x, y;
  unsigned int a, b;

  x = 1; y = -1;
  a = 1; b = -1;

  printf("x = %d\n",x);
  printf("x = %u\n",x);

  printf("y = %d\n",y);
  printf("y = %u\n",y);

  printf("a = %d\n",a);
  printf("a = %u\n",a);
  
  printf("b = %d\n",b);
  printf("b = %u\n",b);

  return 0;
}
